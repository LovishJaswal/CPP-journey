// This code snippet demonstrates basic input and output in C++
// It takes an integer input from the user and prints it back.

#include<iostream>
using namespace std;

int main() {
    int x;

    // Prompt the user for input
    cout << "Enter an integer: ";
    cin >> x;

    // Display the entered value
    cout << "You entered: " << x << endl;

    // Output:
    // Enter an integer: 5
    // You entered: 5

    return 0;
}
